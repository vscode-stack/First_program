print("Driving licence checker\n-------------------------")

age = int(input('Enter your age:'))

if(age > 18):
    print("Your age is eligible for driving licence.")

elif(age < 18):
    print("Your age is not eligible for driving licence.")

else:
    print("Please visit our national licence eligibility counciler")


    


 
 

